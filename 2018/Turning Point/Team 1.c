int distanceToDriveTicks(float distance) {
	return (int) (COUNTS_PER_MOTOR_ROTATION[DRIVE_MOTORS_GEARING] * distance / METERS_PER_WHEEL_ROTATION); // May be off by up to *gasp* 1 tick due to rounding down
}

